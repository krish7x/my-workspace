/**
 * @param {number[]} nums1
 * @param {number[]} nums2
 * @return {number[]}
 */
var intersection = function (nums1, nums2) {
  if (nums2[0] === num2[1]) {
    return [nums2[0]];
  } else {
    return [nums2[0], nums2[1]];
  }
};
